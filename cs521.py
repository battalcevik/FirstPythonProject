
# Simple program to calculate the area of square
input_str = input('Enter side of a square: ')
side = float(input_str)
area = side * side
print('area of the square: ', area)

# Simple program to calculate the volume of cube
input_cube = input('Enter side of cube: ')
side_cube = float(input_cube)
volume = side_cube * side_cube * side_cube
print('the volume of the cube: ', volume)

